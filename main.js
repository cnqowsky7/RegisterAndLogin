register = function () {
    reg = document.getElementById('reg');
    log = document.getElementById('log');

    reg.style.display = "grid";
    log.style.display = "none";
}

login = function () {
    reg = document.getElementById('reg');
    log = document.getElementById('log');

    reg.style.display = "none";
    log.style.display = "grid";
}